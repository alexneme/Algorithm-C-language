/*

  Course Pack 1 - Variables
  Exercise 8. Write a program that reads two integers, calculates and displays the first number
  multiplied by 5 and the second number added to 10.

*/

#include <stdio.h>

int main()
{
    int n1, n2;
    printf("Enter first integer number: ");
    scanf("%d", &n1);
    printf("Enter second integer number: ");
    scanf("%d", &n2);
    printf("First number times 5 = %d\nSecond number plus 10 = %d", n1 = n1 * 5, n2 = n2 + 10);
	return 0;
}